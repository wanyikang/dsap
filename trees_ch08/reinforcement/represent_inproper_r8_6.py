# Inproper binary tree T has n nodes, if we want to convert it to a proper
# tree by filling with dummy nodes, How many nodes do we need at least?
# A binary node has at most two children, so nodes with two or zero children do
# not need filling, let f be the number of nodes need filling, then 0<= f <= n.
# So we need f dummy nodes to convert the inproper binary tree to proper
# binary tree. So the proper tree has O(n) nodes.

